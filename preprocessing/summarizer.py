def execute_parallel_llm(func, args_list, max_workers):
    """

    Args:
        func: 执行方法
        args_list: 方法参数列表，大模型为 query、llm_kwargs
        max_workers: 最大并发数

    Returns:
        执行结果列表
    """
    with concurrent.futures.ThreadPoolExecutor(max_workers=max_workers) as executor:
        future_to_args = {
            executor.submit(func, arg[0], **arg[1]): arg for arg in args_list
        }
        results = []
        for future in concurrent.futures.as_completed(future_to_args):
            original_args = future_to_args[future]
            try:
                result = future.result()
                index = args_list.index(original_args)
                results.append((index, result))
            except Exception as e:
                results.append((args_list.index(original_args), f"Error: {e}"))
        results_in_order = sorted(results, key=lambda x: x[0])
        return [result for index, result in results_in_order]


def initial_summary_generation_modelops(voice_title_list, 
                                custom_prompt_template=custom_prompt_template, 
                                llm=qwen3_handler, 
                                model="Qwen3_8B_10K_Chat_20250627_BF16_vLLM_2A10V2-v1", 
                                is_think=False,
                                max_workers=None):
    if max_workers is None:
        max_workers = LLM_VOICE_SUMMARY_MAX_WORKERS

    llm_kwargs = {
        "model": model,
        "is_think": is_think
    }

    task_prompt_list = []
    for voice_title in voice_title_list:
        task_prompt = custom_prompt_template.format(feedback=voice_title)
        task_prompt_list.append(task_prompt)

    args_list = [(task_prompt, llm_kwargs) for task_prompt in task_prompt_list]

    results_from_llm = execute_parallel_llm(llm, args_list, max_workers=max_workers)

    results = []
    for voice_title, result in zip(voice_title_list, results_from_llm):
        result_dict = {
            'voice_title': voice_title,
            'initial_summary_text': result,
            'initial_summary_id': generate_initial_summary_id(),
            
            # V2上线后，这2个字段没用
            'risk_event_title': result,
            'risk_event_id': generate_initial_summary_id(),
        }
        results.append(result_dict)

    return results


